    public void buscarBotica() {
        Boticas c = null;
        try {
            c = DBBoticas.buscarBotica(txtIdBotica.getText(), null);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "ERROR:" + e.getMessage());
        }
        if (c != null) {
            txtIdBotica.setText(String.valueOf(c.getId_Botica()));
            txtDescripcion.setText(c.getDescripcion());
            //txtIdZona.setText(String.valueOf(c.getId_Zona()));
            txtAcronimo.setText(c.getAcronimo());
            txtCodDescargo.setText(c.getCod_Descargo());

        } else {
            JOptionPane.showMessageDialog(null, "No exite");
        }
    }

