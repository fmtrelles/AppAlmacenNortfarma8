    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (txtIdBotica.getText().compareTo("") != 0 && txtDescripcion.getText().compareTo("") != 0) {
            Boticas c = new Boticas() {
            };

            c.setId_Botica(txtIdBotica.getText());
            c.setDescripcion(txtDescripcion.getText());
            //c.setId_Zona(Integer.parseInt(txtIdZona.getText()));
            c.setId_Zona(Integer.parseInt(cboZona.getSelectedItem().toString().substring(0, 3)));
            c.setAcronimo(txtAcronimo.getText());
            c.setCod_Descargo(txtCodDescargo.getText());
            try {
                DBBoticas.insertarBoticas(c);
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error BD: " + e.getMessage());
            }
            JOptionPane.showMessageDialog(null, "Datos Agregados Correctamente");
            limpiarTextos();
        } else {
            JOptionPane.showMessageDialog(null, "Llene Todos Los Campos..OKz");
        }
        txtIdBotica.setEditable(true);
        btnNuevo.setEnabled(true);
        btnEditar.setEnabled(false);
        btnEliminar.setEnabled(false);
        btnGuardar.setEnabled(false);
        btnCancelar.setEnabled(false);
        btnBuscar.setEnabled(true);
    }//GEN-LAST:event_btnGuardarActionPerformed

