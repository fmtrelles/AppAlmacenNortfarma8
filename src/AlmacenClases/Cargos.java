public class Cargos {
	private int Id_Cargo;
	private String Descripcion;
	private String DiasCargo;

    /**
     * @return the Id_Cargo
     */
    public int getId_Cargo() {
        return Id_Cargo;
    }

    /**
     * @param Id_Cargo the Id_Cargo to set
     */
    public void setId_Cargo(int Id_Cargo) {
        this.Id_Cargo = Id_Cargo;
    }

    /**
     * @return the Descripcion
     */
    public String getDescripcion() {
        return Descripcion;
    }

    /**
     * @param Descripcion the Descripcion to set
     */
    public void setDescripcion(String Descripcion) {
        this.Descripcion = Descripcion;
    }

    /**
     * @return the DiasCargo
     */
    public String getDiasCargo() {
        return DiasCargo;
    }

    /**
     * @param DiasCargo the DiasCargo to set
     */
    public void setDiasCargo(String DiasCargo) {
        this.DiasCargo = DiasCargo;
    }

	

}
